public class Swapping2 {

	
	
	public static void main(String[] args) {
		
		
		//without using temp
		int a=56,b=23;
		
		
		 a=a+b; //56+23=79 ;a=79
		 b=a-b; //79-23=56 ;b=56
		 a=a-b;  //79-56=23; a=23
		
		
		System.out.println(a);
		
		System.out.println(b);
		
		
		
		
		
		
	}
	
	
	
	
	

}
