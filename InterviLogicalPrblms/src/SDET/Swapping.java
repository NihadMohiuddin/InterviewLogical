package SDET;

public class Swapping {

	
	public static void main(String[] args) {
		
		
		int a=10,b=15,temp;
		
		
		temp=a; //temp=10// our aim is to empty a 
		
		a=b;   //a=15
		b=temp;  //b=10
		
		System.out.println(a);
		System.out.println(b);
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
