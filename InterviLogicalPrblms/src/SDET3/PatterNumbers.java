public class PatterNumbers {
	  public static void main(String[] args) {

/*
1
12
123
1234
12345

 * */


	        int rowcount=5;

	        for (int i=0;i<rowcount;i++)
	        {

	            for (int j=0;j<=i;j++)
	            {
	                
	            	System.out.print(j+1);




	            }
	            System.out.println("");

	        }




	  }
}
