package SDET3;

public class PatternNum {

	
	public static void main(String[] args) {
		
		
	 	
/*       
	     *   123456
		 *   12345
		 *   1234
		 *   123
		 *   12
		 *   1
*/
		
		int countRow=6;
		
		for (int i = countRow; i >0; i--)
		{
			
			
			for (int j = 0; j <i; j++)
			{
			      
				System.out.print(j+1); //1
				
			}
		
		
		         System.out.println("");
		
		
		}
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
}
