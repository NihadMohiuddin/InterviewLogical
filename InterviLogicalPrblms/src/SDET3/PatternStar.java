package SDET3;
import java.util.Iterator;

public class PatternStar {

	
	
	public static void main(String[] args) {
		
/*	
		* 
		* * 
		* * * 
		* * * * 
		* * * * * 
		
*/
		int Rowcount=6;
		
		for (int i = 0; i <Rowcount; i++)
		{
			
			for (int j = 0; j <=i ; j++) 
			{
				
				System.out.print("*");
				
				
			}
			
			System.out.println("");
			
			
		}
		
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
}
